    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Informacion:");
        DefaultMutableTreeNode nd_DRs = new DefaultMutableTreeNode("Doctores");
        DefaultMutableTreeNode nd_PCs = new DefaultMutableTreeNode("Pacientes");

        for (int i = 0; i < listPaciente.size(); i++) {
            DefaultMutableTreeNode nd_PC = new DefaultMutableTreeNode(listPaciente.get(i));
            for (int j = 0; j < listPaciente.get(i).getListOrganos().size(); j++) {
                DefaultMutableTreeNode nd_enf = new DefaultMutableTreeNode(listPaciente.get(i).listOrganos.get(j));
                nd_enf.add(new DefaultMutableTreeNode("Efectividad: " + listPaciente.get(i).listOrganos.get(j).NVL_Efectividad));
                nd_enf.add(new DefaultMutableTreeNode("Potenciado: " + listPaciente.get(i).listOrganos.get(j).NVL_Potenciado));
                nd_enf.add(new DefaultMutableTreeNode("Prueba: " + listPaciente.get(i).listOrganos.get(j).NVL_prueba));
                nd_PC.add(nd_enf);
            }
            nd_PCs.add(nd_PC);
        }
        root.add(nd_PCs);
        DefaultTreeModel model = new DefaultTreeModel(root);
        jTree1.setModel(model);

        jDialog3.pack();
        jDialog3.setVisible(true);
    }//GEN-LAST:event_jMenuItem3ActionPerformed

