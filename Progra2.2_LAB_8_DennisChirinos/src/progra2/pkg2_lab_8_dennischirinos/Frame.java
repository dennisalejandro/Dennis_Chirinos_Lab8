    public Paciente CrearPaciente(int Serie) {
        String Nombre = JD1_TF_Nombre.getText();
        int Edad = (int) JD1_SP_Edad.getValue();
        int ID = (int) JD1_SP_ID.getValue();
        double Altura = Double.parseDouble(JD1_TF_Altura.getText());
        double Peso = Double.parseDouble(JD1_TF_Peso.getText());
        String Sexo;
        String Sangre = JD1_TF_Sangre.getText();
        if (JD1_CB_M.isSelected()) {
            Sexo = "Hombre";
        } else if (JD1_CB_F.isSelected()) {
            Sexo = "Mujer";
        } else {
            Sexo = "?";
        }
        Date Entrada = jDateChooser_Entrada.getDate();
        Date Alta = jDateChooser_Salida.getDate();
        Paciente c = new Paciente(Entrada, Alta, Sangre, Sexo, Nombre, Edad, ID, Altura, Peso, Sexo);
        return c;
    }

